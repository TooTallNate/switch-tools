/**
 * Generator output: emit the files needed to build one extension.
 *
 * Each extension lives in its own directory and contains:
 *
 *   - `Makefile`     — clang invocations to compile the resolved
 *                       object files + link them into `<name>.so`.
 *
 *   - `init.c`       — exports one `ffmpeg_ext_<sym>_<kind>` accessor
 *                       per registered codec / demuxer / muxer.
 *
 *   - `shim.h`       — `#define CONFIG_X 1` lines for every flag
 *                       the resolver said we need active. Included
 *                       on the clang command line BEFORE the
 *                       FFmpeg headers so codec source files don't
 *                       DCE their own decode paths.
 *
 *   - `manifest.json`— what's in this extension (kind, names,
 *                       size). Aggregated up into the package-wide
 *                       `extensions-manifest.json`.
 *
 * The output is intentionally deterministic: every list is sorted,
 * paths are relative, no timestamps. Running the generator twice
 * on the same input produces byte-identical files.
 */
import type { ResolveResult } from "./resolve.ts"

/** Kinds of things we can register. */
export type ExtensionKind = "decoder" | "encoder" | "demuxer" | "muxer"

export interface ExtensionEntry {
	/** Lowercase canonical name (e.g. `"aac_decoder"`). */
	thing: string
	/** Which kind of upstream object this is. */
	kind: ExtensionKind
	/**
	 * Upstream C symbol of the codec/demuxer/muxer struct.
	 * Always `ff_<thing>` per FFmpeg convention.
	 */
	symbol: string
	/**
	 * C type of the upstream struct:
	 *   - `decoder` / `encoder` → `FFCodec`
	 *   - `demuxer`             → `AVInputFormat`
	 *   - `muxer`               → `FFOutputFormat`
	 */
	cType: string
}

export interface EmittedFiles {
	"Makefile": string
	"init.c": string
	"shim.h": string
	"manifest.json": string
}

export interface EmitOptions {
	/**
	 * Slug used for the extension directory and `.so` filename
	 * (e.g. `"aac-decoder"`). Convention: lowercase + dashes.
	 */
	slug: string
	/**
	 * One-line human description (rendered into manifest.json and
	 * the Makefile's header comment).
	 */
	description: string
	/**
	 * Entries this extension registers. Typically one entry, but
	 * we allow several when multiple closely-related codecs share
	 * compile units (e.g. binkaudio_dct + binkaudio_rdft).
	 */
	entries: ExtensionEntry[]
	/**
	 * Resolver output: object files needed + CONFIG flags.
	 */
	resolved: ResolveResult
	/**
	 * Path from the extension's directory back to the base
	 * `ffmpeg-wasm` package, used by the Makefile to locate the
	 * upstream sources. Defaults to `"../.."` which corresponds
	 * to the layout `packages/ffmpeg-wasm/src/extensions/<slug>/`.
	 */
	baseRel?: string
}

export function emitFiles(opts: EmitOptions): EmittedFiles {
	const baseRel = opts.baseRel ?? "../../.."
	const ffmpegRel = `${baseRel}/build/ffmpeg`

	return {
		"Makefile": renderMakefile(opts, baseRel, ffmpegRel),
		"init.c": renderInitC(opts),
		"shim.h": renderShim(opts),
		"manifest.json": renderManifest(opts),
	}
}

// ---------------------------------------------------------------------------
// Makefile
// ---------------------------------------------------------------------------

function renderMakefile(opts: EmitOptions, baseRel: string, ffmpegRel: string): string {
	const objs = opts.resolved.objs.slice().sort((a, b) =>
		`${a.library}/${a.obj}`.localeCompare(`${b.library}/${b.obj}`),
	)

	const buildTargets: string[] = []
	for (const { library, obj } of objs) {
		const src = obj.replace(/\.o$/, ".c")
		const buildName = `${library}_${obj}`
		buildTargets.push(
			`build/${buildName}: ${ffmpegRel}/${library}/${src} | build check-base\n` +
				`\t$(CC) $(BASE_CFLAGS) -c -o $@ $<`,
		)
	}

	const objList = objs.map(({ library, obj }) => `build/${library}_${obj}`)
	const objListStr = objList.join(" ") + " build/init.o"

	return `# @tootallnate/ffmpeg-wasm/extensions/${opts.slug}
#
# ${opts.description}
#
# AUTOGENERATED by scripts/gen/. Do not edit by hand — re-run
# the generator after changing the upstream FFmpeg checkout.

WASI_SDK_VERSION ?= 25
WASI_SDK ?= /tmp/wasi-sdk
CC      = $(WASI_SDK)/bin/clang
WASM_LD = $(WASI_SDK)/bin/wasm-ld

SYSROOT = $(WASI_SDK)/share/wasi-sysroot

FFMPEG_BASE = ${ffmpegRel}
SHARED_INC  = ${baseRel}/c

OUT = ${opts.slug}.so

BASE_CFLAGS = \\
\t--target=wasm32-wasip1 \\
\t--sysroot=$(SYSROOT) \\
\t-fPIC \\
\t-fvisibility=hidden \\
\t-O2 \\
\t-DHAVE_AV_CONFIG_H \\
\t-I$(FFMPEG_BASE) \\
\t-I$(FFMPEG_BASE)/libavcodec \\
\t-I$(FFMPEG_BASE)/libavformat \\
\t-I$(SHARED_INC) \\
\t-include shim.h \\
\t-include libavutil/internal.h \\
\t-Wno-implicit-fallthrough \\
\t-Wno-deprecated-declarations \\
\t-Wno-incompatible-pointer-types \\
\t-Wno-unused-but-set-variable \\
\t-Wno-unused-variable \\
\t-Wno-unused-function \\
\t-Wno-pointer-sign \\
\t-Wno-bitwise-op-parentheses \\
\t-Wno-shift-op-parentheses

LDFLAGS = \\
\t--shared \\
\t--no-entry \\
\t--export-dynamic \\
\t--allow-undefined \\
\t--gc-sections

.PHONY: all clean check-base

all: check-base $(OUT)

check-base:
\t@if [ ! -d "$(FFMPEG_BASE)" ]; then \\
\t\techo "ERROR: base FFmpeg checkout not found at $(FFMPEG_BASE)"; \\
\t\techo "Run 'make setup-source' in $(realpath $(FFMPEG_BASE)/../..) first."; \\
\t\texit 1; \\
\tfi

build:
\t@mkdir -p build

build/init.o: init.c shim.h | build check-base
\t$(CC) $(BASE_CFLAGS) -c -o $@ $<

${buildTargets.join("\n\n")}

$(OUT): ${objListStr}
\t$(WASM_LD) $(LDFLAGS) -o $@ ${objListStr}
\t@echo "Built $(OUT) ($$(wc -c < $(OUT) | tr -d ' ') bytes)"

clean:
\trm -rf build $(OUT)
`
}

// ---------------------------------------------------------------------------
// init.c
// ---------------------------------------------------------------------------

function renderInitC(opts: EmitOptions): string {
	const decls: string[] = []
	const exports: string[] = []
	let codecIdx = 0
	let demuxerIdx = 0
	let muxerIdx = 0

	for (const e of opts.entries) {
		decls.push(`extern const struct ${e.cType} ${e.symbol};`)

		// Pick the export name based on kind, mirroring the convention
		// the loader's regex expects:
		//   ffmpeg_ext_<slug>_codec(_N)?
		//   ffmpeg_ext_<slug>_demuxer(_N)?
		//   ffmpeg_ext_<slug>_muxer(_N)?
		let exportName: string
		const slugSym = opts.slug.replaceAll("-", "_")
		switch (e.kind) {
			case "decoder":
			case "encoder":
				codecIdx++
				exportName = `ffmpeg_ext_${slugSym}_codec_${codecIdx}`
				break
			case "demuxer":
				demuxerIdx++
				exportName = `ffmpeg_ext_${slugSym}_demuxer_${demuxerIdx}`
				break
			case "muxer":
				muxerIdx++
				exportName = `ffmpeg_ext_${slugSym}_muxer_${muxerIdx}`
				break
		}

		exports.push(
			`__attribute__((visibility("default")))\n` +
				`const struct ${e.cType} *${exportName}(void)\n` +
				`{\n` +
				`\treturn &${e.symbol};\n` +
				`}`,
		)
	}

	return `/**
 * Extension init for @tootallnate/ffmpeg-wasm/extensions/${opts.slug}.
 *
 * ${opts.description}
 *
 * AUTOGENERATED by scripts/gen/. Do not edit by hand — re-run
 * the generator after changing the upstream FFmpeg checkout.
 */

#include <stddef.h>

${decls.join("\n")}

${exports.join("\n\n")}
`
}

// ---------------------------------------------------------------------------
// shim.h
// ---------------------------------------------------------------------------

function renderShim(opts: EmitOptions): string {
	// Pull in upstream's `config.h` + `config_components.h` FIRST
	// (which `#define CONFIG_X 0` for everything our base build
	// disables), then override the flags this extension needs.
	// C preprocessor rule: last `#define` wins.
	//
	// We can't rely on the compile-line `-include libavutil/internal.h`
	// here because that runs after this shim, NOT before. Including
	// the configs ourselves up front is the cleanest fix and keeps
	// the upstream config defines (HAVE_X, ARCH_X, ...) intact for
	// the FFmpeg sources that depend on them.
	const lines = opts.resolved.configFlags.slice().sort()

	return `/*
 * Build-time shim for the ${opts.slug} extension.
 *
 * \`-include\`d before any FFmpeg source compiles. We:
 *
 *   1. Pull in upstream's \`config.h\` + \`config_components.h\`
 *      so all the HAVE_X / ARCH_X / CONFIG_X defines exist.
 *
 *   2. \`#undef\` + \`#define CONFIG_X 1\` for every flag this
 *      extension needs active. C preprocessing rule: last
 *      define wins, so our overrides take effect for all
 *      subsequent source compiles.
 *
 * Without step 2, FFmpeg's per-codec source files DCE their
 * own decode/encode paths because the base build set every
 * \`CONFIG_X\` to 0.
 *
 * AUTOGENERATED by scripts/gen/. Do not edit by hand.
 */
#ifndef TTN_EXT_${opts.slug.replaceAll("-", "_").toUpperCase()}_SHIM_H
#define TTN_EXT_${opts.slug.replaceAll("-", "_").toUpperCase()}_SHIM_H

#include "config.h"
#include "config_components.h"

${lines.map((f) => `#undef CONFIG_${f}\n#define CONFIG_${f} 1`).join("\n")}

#endif
`
}

// ---------------------------------------------------------------------------
// manifest.json
// ---------------------------------------------------------------------------

function renderManifest(opts: EmitOptions): string {
	const payload = {
		slug: opts.slug,
		description: opts.description,
		entries: opts.entries
			.slice()
			.sort((a, b) => a.thing.localeCompare(b.thing))
			.map((e) => ({ thing: e.thing, kind: e.kind, symbol: e.symbol })),
		objs: opts.resolved.objs
			.slice()
			.sort((a, b) =>
				`${a.library}/${a.obj}`.localeCompare(`${b.library}/${b.obj}`),
			)
			.map((o) => `${o.library}/${o.obj}`),
		configFlags: opts.resolved.configFlags.slice().sort(),
		baseHelpers: opts.resolved.resolvedFromBase.slice().sort(),
	}
	return JSON.stringify(payload, null, 2) + "\n"
}
