/**
 * SEAD material codec IDs (`mtrl[0x05]`) and their corresponding
 * audio formats. Values from vgmstream's `parse_sead()`.
 */
export const SEAD_CODEC = {
	DUMMY: 0x00,
	PCM16LE: 0x01,
	MSADPCM: 0x02,
	OGG_VORBIS: 0x03,
	ATRAC9: 0x04,
	XMA2: 0x05,
	MSMP3: 0x06,
	HCA: 0x07,
	SWITCH_OPUS: 0x08,
} as const;

export type SeadCodec = (typeof SEAD_CODEC)[keyof typeof SEAD_CODEC];

/** Human-readable codec name. */
export function codecName(codec: number): string {
	switch (codec) {
		case SEAD_CODEC.DUMMY:
			return 'dummy';
		case SEAD_CODEC.PCM16LE:
			return 'pcm16le';
		case SEAD_CODEC.MSADPCM:
			return 'ms-adpcm';
		case SEAD_CODEC.OGG_VORBIS:
			return 'ogg-vorbis';
		case SEAD_CODEC.ATRAC9:
			return 'atrac9';
		case SEAD_CODEC.XMA2:
			return 'xma2';
		case SEAD_CODEC.MSMP3:
			return 'ms-mp3';
		case SEAD_CODEC.HCA:
			return 'hca';
		case SEAD_CODEC.SWITCH_OPUS:
			return 'switch-opus';
		default:
			return `unknown-${codec.toString(16)}`;
	}
}

