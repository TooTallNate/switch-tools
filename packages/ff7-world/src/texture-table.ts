export type WorldMapKind = 'overworld' | 'underwater' | 'glacier';

/**
 * Pick the right texture table for a given world-map file. Use
 * the file's section count as the discriminator (68 / 12 / 4).
 */
export function texturesForMap(kind: WorldMapKind): readonly WorldTextureInfo[] {
	switch (kind) {
		case 'overworld':
			return OVERWORLD_TEXTURES;
		case 'underwater':
			return UNDERWATER_TEXTURES;
		case 'glacier':
			return GLACIER_TEXTURES;
	}
}

export function kindFromSectionCount(sectionCount: number): WorldMapKind {
	if (sectionCount >= 60) return 'overworld';
	if (sectionCount >= 8) return 'underwater';
	return 'glacier';
}
