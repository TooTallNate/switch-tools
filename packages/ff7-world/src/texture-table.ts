/**
 * Hardcoded WMSET texture metadata tables. These tables are baked
 * into the FF7 PC engine and replicated verbatim by every known
 * reimplementation (q-gears, V-Gears, Braver, ff7-landscaper).
 *
 * Each triangle's `(rawU, rawV)` is converted to per-texture UVs via:
 *
 *   u = (rawU - tex.uOffset) / tex.width
 *   v = (rawV - tex.vOffset) / tex.height
 *
 * The result may be negative or > 1 — sample with WRAP/REPEAT.
 *
 * The named `.tex` files live in `world_us.lgp` (or the matching
 * `world_de.lgp` / `world_fr.lgp` / etc.).
 */

export interface WorldTextureInfo {
	id: number;
	/** Base name; the on-disk file is `<name>.tex` inside `world_us.lgp`. */
	name: string;
	width: number;
	height: number;
	uOffset: number;
	vOffset: number;
}

