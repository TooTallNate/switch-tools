/**
 * The IMDCT-like synthesis stage of HCA's decoder needs three
 * heavy lookup tables. Splitting them into their own module keeps
 * `tables.ts` readable.
 *
 * Each `LIST1_FLOAT[i]` and `LIST2_FLOAT[i]` is a 0x80-entry table
 * for synthesis stage `i` (i ∈ [0, 7)). `LIST3_FLOAT` is a single
 * 0x80-entry windowing table used at the very end of decode5.
 *
 * Bit patterns are verbatim from kohos/CriTools/src/hca.js (MIT).
 */

function u32ToFloat32(values: number[]): Float32Array {
	const buf = new ArrayBuffer(4);
	const dv = new DataView(buf);
	const out = new Float32Array(values.length);
	for (let i = 0; i < values.length; i++) {
		dv.setUint32(0, values[i]! >>> 0, true);
		out[i] = dv.getFloat32(0, true);
	}
	return out;
}

