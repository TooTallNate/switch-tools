function decodeBlock(
	header: HcaHeader,
	state: DecoderState,
	athTable: Uint8Array,
	ciphTable: Uint8Array,
	source: Uint8Array,
	address: number,
): void {
	// Copy the encoded block out of the source so we don't mutate
	// the caller's buffer when applying the cipher.
	const block = new Uint8Array(header.blockSize);
	const slice = source.subarray(address, address + header.blockSize);
	block.set(slice);

	// On a CRC-16 failure (observed in some real-world HCAs, e.g.
	// FF1 Pixel Remaster BGM where some blocks have invalid CRCs),
	// silence the block: zero the wave output AND the IMDCT
	// overlap-add carry, then skip the decode body. This matches
	// vgmstream's clHCA behaviour. CriTools throws here, which is
	// stricter than vgmstream — and breaks on files like FF1's
	// BGM that play fine in every other tool.
	// CRC-16 self-check. CriTools throws on a mismatch, but
	// real-world HCAs sometimes contain blocks with bad CRCs
	// that vgmstream silences. We do the same: zero the wave
	// output and bail. (Strict-CRC HCAs are unaffected.)
	if (checkSum(block, header.blockSize) !== 0) {
		for (let c = 0; c < header.channelCount; c++) {
			const ch = state.channels[c]!;
			for (let i = 0; i < 8; i++) ch.wave[i]!.fill(0);
		}
		return;
	}
	if (header.ciphType) decryptBlock(ciphTable, block);

	const reader = new BlockReader(block);
	const magic = reader.getBit(16);
	// The 0xFFFF sync magic indicates a fully-encoded block. A
	// mismatched magic means "encoded silence" — leave the per-
	// channel state alone and the previous block's PCM is what
	// gets emitted by the caller. (Same as CriTools.)
	if (magic === 0xffff) {
		const a = (reader.getBit(9) << 8) - reader.getBit(7);
		const comp = state.comp;
		for (let i = 0; i < header.channelCount; i++) {
			decode1(state.channels[i]!, reader, comp.r09, a, athTable);
		}
		for (let i = 0; i < 8; i++) {
			for (let j = 0; j < header.channelCount; j++) {
				decode2(state.channels[j]!, reader);
			}
			for (let j = 0; j < header.channelCount; j++) {
				decode3(
					state.channels[j]!,
					comp.r09,
					comp.r08,
					comp.r07 + comp.r06,
					comp.r05,
				);
			}
			for (let j = 0; j < header.channelCount - 1; j++) {
				decode4(
					state.channels[j]!,
					state.channels[j + 1]!,
					i,
					comp.r05 - comp.r06,
					comp.r06,
					comp.r07,
				);
			}
			for (let j = 0; j < header.channelCount; j++) {
				decode5(state.channels[j]!, i);
			}
		}
	}
	// magic != 0xFFFF → silence block; channel state untouched.
}
