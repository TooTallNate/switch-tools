/*
 * HCA block decoder.
 *
 * Per encoded block:
 *   1. Optional cipher decrypt (in-place on a per-block copy).
 *   2. Read sync magic = 0xFFFF + 16-bit "a" value.
 *   3. For each channel: decode1 reads scale-factors + quant
 *      lookup state.
 *   4. For each of 8 sub-frames:
 *        decode2  read quantised coefficients into channel.block
 *        decode3  intensity-stereo / joint-stereo expand
 *        decode4  M/S coupling
 *        decode5  inverse MDCT + windowing → 128 PCM samples per
 *                 channel into channel.wave[sub].
 *   5. Caller interleaves 8 × 128 = 1024 PCM samples per channel
 *      into the global output buffer.
 *
 * All the big static tables (DECODE1..DECODE5) are stored as
 * `readonly` arrays of the original IEEE-754 bit patterns and
 * converted to Float32 once at module load.
 *
 * Ported from kohos/CriTools (MIT) — https://github.com/kohos/CriTools
 */

import { parseHca, type HcaHeader, HcaParseError } from './parse.js';
import {
	initCiphTable,
	decryptBlock,
	combineAwbKey,
	splitKey64,
} from './decrypt.js';

/** Options for {@link decodeHca}. */
export interface DecodeOptions {
	/**
	 * 64-bit HCA cipher key as a `bigint`. Unused for type-0
	 * (unencrypted) and type-1 (static-table) cipher headers.
	 */
	key?: bigint;
	/**
	 * AWB sub-key. Combined with `key` for the final 64-bit
	 * cipher seed when the HCA was extracted from an AWB.
	 * Unused for standalone HCAs.
	 */
	awbKey?: number;
	/** Volume multiplier; defaults to 1.0. */
	volume?: number;
}

export interface DecodedHca {
	channelCount: number;
	samplingRate: number;
	/** Total number of samples per channel (NOT total floats). */
	samplesPerChannel: number;
	/**
	 * Interleaved Float32 PCM: `[ch0_s0, ch1_s0, ch0_s1, ...]`.
	 * Length is `samplesPerChannel * channelCount`.
	 */
	pcm: Float32Array;
	loopStart?: number;
	loopEnd?: number;
}

/**
 * Decode an HCA file end-to-end into a flat Float32 PCM buffer.
 */
export function decodeHca(
	bytes: Uint8Array,
	options: DecodeOptions = {},
): DecodedHca {
	const volume = options.volume ?? 1.0;
	const header = parseHca(bytes);

	// Resolve the cipher key.
	let key1 = 0;
	let key2 = 0;
	if (options.key !== undefined) {
		let k = options.key & 0xffffffffffffffffn;
		if (options.awbKey) k = combineAwbKey(k, options.awbKey);
		[key1, key2] = splitKey64(k);
	}

	// Build the ATH and CIPH tables once per decode.
	const athTable = new Uint8Array(0x80);
	if (!initAthTable(athTable, header.athType, header.samplingRate)) {
		throw new HcaParseError(`Bad athType: ${header.athType}`);
	}
	const ciphTable = new Uint8Array(0x100);
	if (!initCiphTable(ciphTable, header.ciphType, key1, key2)) {
		throw new HcaParseError(`Bad ciphType: ${header.ciphType}`);
	}

	const state = initDecode(header);

	const samplesPerChannel = header.blockCount * 8 * 0x80;
	const pcm = new Float32Array(samplesPerChannel * header.channelCount);
	const baseVolume = (header.volume ?? 1.0) * volume;

	let address = header.dataOffset;
	let n = 0;
	for (let m = 0; m < header.blockCount; m++) {
		decodeBlock(header, state, athTable, ciphTable, bytes, address);
		// Interleave 8 × 128 per channel → flat output.
		for (let i = 0; i < 8; i++) {
			for (let j = 0; j < 0x80; j++) {
				for (let k = 0; k < header.channelCount; k++) {
					pcm[n++] = state.channels[k]!.wave[i]![j]! * baseVolume;
				}
			}
		}
		address += header.blockSize;
	}

	return {
		channelCount: header.channelCount,
		samplingRate: header.samplingRate,
		samplesPerChannel,
		pcm,
		loopStart: header.loopStart,
		loopEnd: header.loopEnd,
	};
}

