const BIT_MASK: readonly number[] = [
	0xffffff, 0x7fffff, 0x3fffff, 0x1fffff, 0x0fffff, 0x07ffff, 0x03ffff,
	0x01ffff,
];

class BlockReader {
	data: Uint8Array;
	size: number;
	bit: number;

	constructor(buffer: Uint8Array) {
		this.data = buffer;
		// Total readable bits = (length * 8) - 16, with the trailing
		// 16 bits being the block checksum (BE u16).
		this.size = buffer.length * 8 - 16;
		this.bit = 0;
	}

	checkBit(bitSize: number): number {
		let v = 0;
		if (this.bit + bitSize <= this.size) {
			const pos = this.bit >>> 3;
			v = this.data[pos]!;
			v = (v << 8) | this.data[pos + 1]!;
			v = (v << 8) | this.data[pos + 2]!;
			v &= BIT_MASK[this.bit & 7]!;
			v >>>= 24 - (this.bit & 7) - bitSize;
		}
		return v;
	}

	getBit(bitSize: number): number {
		const v = this.checkBit(bitSize);
		this.bit += bitSize;
		return v;
	}

	addBit(bitSize: number): void {
		this.bit += bitSize;
	}
}

// ---------------------------------------------------------------------------
// Static float tables — stored as u32 IEEE-754 bit patterns and decoded
// once on module load.
// ---------------------------------------------------------------------------

function intsToFloats(ints: readonly number[]): Float32Array {
	const out = new Float32Array(ints.length);
	const tmp = new ArrayBuffer(4);
	const dv = new DataView(tmp);
	for (let i = 0; i < ints.length; i++) {
		dv.setUint32(0, ints[i]! >>> 0, false);
		out[i] = dv.getFloat32(0, false);
	}
	return out;
}

