interface ChannelState {
	block: Float32Array;
	base: Float32Array;
	/** Per-coefficient scale-factor (`channel.value`). */
	value: Uint8Array;
	/** Quantiser bits per coefficient (`channel.scale`). */
	scale: Uint8Array;
	/** Intensity-stereo state (`channel.value2`). */
	value2: Uint8Array;
	/**
	 * Slice of `value` starting at `r06+r07`. Stays as a
	 * mutable view onto the same backing storage as `value`.
	 */
	value3: Uint8Array;
	type: number;
	count: number;
	wav1: Float32Array;
	wav2: Float32Array;
	wav3: Float32Array;
	wave: Float32Array[];
}

interface CompState {
	r01: number;
	r02: number;
	r03: number;
	r04: number;
	r05: number;
	r06: number;
	r07: number;
	r08: number;
	r09: number;
}

interface DecoderState {
	comp: CompState;
	channels: ChannelState[];
}

function ceil2(a: number, b: number): number {
	return b > 0 ? Math.floor(a / b) + (a % b ? 1 : 0) : 0;
}

function initDecode(header: HcaHeader): DecoderState {
	const isComp = header.compdec === 'comp';
	const r05 = isComp ? header.r05 : (header.count1 ?? 0) + 1;
	const r06 = isComp
		? header.r06
		: header.enableCount2
			? (header.count2 ?? 0) + 1
			: (header.count1 ?? 0) + 1;
	const r07 = isComp ? header.r07 : r05 - r06;
	const r08 = isComp ? header.r08 : 0;
	let r03 = header.r03;
	if (!r03) r03 = 1;
	if (!(header.r01 === 1 && header.r02 === 15)) {
		throw new HcaParseError(
			`Unsupported r01/r02 combination: ${header.r01}/${header.r02}`,
		);
	}
	const r09 = ceil2(r05 - (r06 + r07), r08);

	// Channel type assignment per sub-channel-group of size `b`.
	const r = new Uint8Array(0x10);
	const b = Math.floor(header.channelCount / r03);
	if (r07 && b > 1) {
		let c = 0;
		for (let i = 0; i < r03; i++) {
			switch (b) {
				case 2:
					r[c] = 1;
					r[c + 1] = 2;
					break;
				case 3:
					r[c] = 1;
					r[c + 1] = 2;
					break;
				case 4:
					r[c] = 1;
					r[c + 1] = 2;
					if (header.r04 === 0) {
						r[c + 2] = 1;
						r[c + 3] = 2;
					}
					break;
				case 5:
					r[c] = 1;
					r[c + 1] = 2;
					if (header.r04 <= 2) {
						r[c + 3] = 1;
						r[c + 4] = 2;
					}
					break;
				case 6:
					r[c] = 1;
					r[c + 1] = 2;
					r[c + 4] = 1;
					r[c + 5] = 2;
					break;
				case 7:
					r[c] = 1;
					r[c + 1] = 2;
					r[c + 4] = 1;
					r[c + 5] = 2;
					break;
				case 8:
					r[c] = 1;
					r[c + 1] = 2;
					r[c + 4] = 1;
					r[c + 5] = 2;
					r[c + 6] = 1;
					r[c + 7] = 2;
					break;
			}
			c += b;
		}
	}

	const channels: ChannelState[] = [];
	for (let i = 0; i < header.channelCount; i++) {
		const value = new Uint8Array(0x80);
		const channel: ChannelState = {
			block: new Float32Array(0x80),
			base: new Float32Array(0x80),
			value,
			scale: new Uint8Array(0x80),
			value2: new Uint8Array(8),
			value3: value.subarray(r06 + r07),
			type: r[i]!,
			count: r06 + (r[i] !== 2 ? r07 : 0),
			wav1: new Float32Array(0x80),
			wav2: new Float32Array(0x80),
			wav3: new Float32Array(0x80),
			wave: [
				new Float32Array(0x80),
				new Float32Array(0x80),
				new Float32Array(0x80),
				new Float32Array(0x80),
				new Float32Array(0x80),
				new Float32Array(0x80),
				new Float32Array(0x80),
				new Float32Array(0x80),
			],
		};
		channels.push(channel);
	}

	return {
		comp: {
			r01: header.r01,
			r02: header.r02,
			r03,
			r04: header.r04,
			r05,
			r06,
			r07,
			r08,
			r09,
		},
		channels,
	};
}

