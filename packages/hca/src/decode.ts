function initAthTable(
	table: Uint8Array,
	type: number,
	key: number,
): boolean {
	if (type === 0) {
		table.fill(0, 0, 0x80);
		return true;
	}
	if (type === 1) {
		let v = 0;
		for (let i = 0; i < 0x80; i++) {
			const index = v >>> 13;
			if (index >= 0x28e) {
				const last = 0x80 - i;
				for (let j = 0; j < last; j++) table[i + j] = 0xff;
				break;
			}
			table[i] = ATH_LIST_1[index]!;
			// v += key (operates mod 2^32, but we only use the
			// top 13+ bits, so plain Number arithmetic is fine).
			v = (v + key) >>> 0;
		}
		return true;
	}
	return false;
}

