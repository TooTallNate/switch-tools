function decode5(channel: ChannelState, index: number): void {
	// Stage 1: 7 butterfly passes between channel.block and channel.wav1.
	{
		let s = channel.block;
		let d = channel.wav1;
		let count1 = 1;
		let count2 = 0x40;
		for (let i = 0; i < 7; i++) {
			let x = 0;
			let d1 = 0;
			let d2 = count2;
			for (let j = 0; j < count1; j++) {
				for (let k = 0; k < count2; k++) {
					const a = s[x++]!;
					const b = s[x++]!;
					d[d1++] = b + a;
					d[d2++] = a - b;
				}
				d1 += count2;
				d2 += count2;
			}
			const w = s;
			s = d;
			d = w;
			count1 <<= 1;
			count2 >>>= 1;
		}
	}
	// Stage 2: 7 passes — now twiddle-factor multiplies; result lands
	// back in channel.block / channel.wav1 alternating; final lives
	// in `s` at end.
	let s: Float32Array;
	let d: Float32Array;
	{
		s = channel.wav1;
		d = channel.block;
		let count1 = 0x40;
		let count2 = 1;
		for (let i = 0; i < 7; i++) {
			const list1Float = DECODE5_LIST1[i]!;
			const list2Float = DECODE5_LIST2[i]!;
			let x = 0;
			let y = 0;
			let s1 = 0;
			let s2 = count2;
			let d1 = 0;
			let d2 = count2 * 2 - 1;
			for (let j = 0; j < count1; j++) {
				for (let k = 0; k < count2; k++) {
					const a = s[s1++]!;
					const b = s[s2++]!;
					const c = list1Float[x++]!;
					const e = list2Float[y++]!;
					d[d1++] = a * c - b * e;
					d[d2--] = a * e + b * c;
				}
				s1 += count2;
				s2 += count2;
				d1 += count2;
				d2 += count2 * 3;
			}
			const w = s;
			s = d;
			d = w;
			count1 >>>= 1;
			count2 <<= 1;
		}
	}
	// Copy `s` into wav2 (the IMDCT output buffer).
	d = channel.wav2;
	for (let i = 0; i < 0x80; i++) d[i] = s[i]!;

	// Windowing & overlap-add into channel.wave[index].
	const sw = DECODE5_LIST3;
	const dw = channel.wave[index]!;
	const s1 = channel.wav2;
	const s2 = channel.wav3;
	for (let i = 0; i < 0x40; i++) {
		dw[i] = s1[0x40 + i]! * sw[i]! + s2[i]!;
	}
	for (let i = 0; i < 0x40; i++) {
		dw[0x40 + i] = sw[0x40 + i]! * s1[0x7f - i]! - s2[0x40 + i]!;
	}
	for (let i = 0; i < 0x40; i++) {
		s2[i] = s1[0x3f - i]! * sw[0x7f - i]!;
	}
	for (let i = 0; i < 0x40; i++) {
		s2[0x40 + i] = sw[0x3f - i]! * s1[i]!;
	}
}

